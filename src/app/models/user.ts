/**
 * Модель пользователя.
 */
export class User {
  /**
   * Идентификатор пользователя.
   */
  id: string;

  /**
   * Логин пользователя.
   */
  login: string;

  /**
   * Пароль пользователя.
   */
  password: string;

  /**
   * Имя пользователя.
   */
  name: string;

  /**
   * Баланс пользователя.
   */
  balance: number;
}
